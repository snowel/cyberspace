func (m BoardEditor) Init() tea.Cmd{
	return nil
}

func (m BoardEditor) Update(msg tea.Msg) (tea.Model, tea.Cmd) {
  switch msg := msg.(type) {

  case tea.KeyMsg:

	 switch msg.String() {
	 case "ctrl+c", "q":
		return m, tea.Quit
	 }

  case tea.MouseMsg:
	 m.Mouse = tea.MouseEvent(msg)
	 inter := TermCoordToIntersect(m.Mouse.X, m.Mouse.Y, 0, 0, dgf.GetSize(m.Board))

	 if inter > 0 {
		m.Curse = inter
		if m.Mouse.Type == tea.MouseRelease {
			PlayMove(m.Board, inter)
			m.LastMove = inter
	    m.Group = dgf.GetGroup(m.Board, m.LastMove)
		}
	 } else {
		m.Curse = 0
	 }
  }

  return m, nil
}

